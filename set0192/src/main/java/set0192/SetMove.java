package set0192;

public class SetMove {
	private String card0;
	private String card1;
	private String card2;

	public SetMove(String card0, String card1, String card2) {
		this.card0 = card0;
		this.card1 = card1;
		this.card2 = card2;
	}

	public String get0() {
		return card0;
	}

	public String get1() {
		return card1;
	}

	public String get2() {
		return card2;
	}

}
